    private void loadTable(){
        for (int i = DataIO.dsNhanVien.size() - 1 ; i >= DataIO.dsNhanVien.size() / 2; i--) {
            DataIO.dsNhanVien.remove(i);
        }
        for(int i = tableNhanVien.getRowCount() - 1; i>=0; i--){
            model.removeRow(i);
        }
        DataIO.loadSach();
        
        
        
        for (NhanVien nv : DataIO.dsNhanVien) {
            Vector vecto = new Vector();
            vecto.add(nv.getMaNhanVien());
            vecto.add(nv.getTenNhanVien());
            vecto.add(nv.getTaiKhoan());
            vecto.add(nv.getNgayVaoLam());
            vecto.add(nv.getSoNgayNghi());
            
            model.addRow(vecto);
        }
    }
