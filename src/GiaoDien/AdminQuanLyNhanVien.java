    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NhanVien NV = new NhanVien();
        NV.setMaNhanVien(txtMaNhanVien.getText());
        NV.setTenNhanVien(txtTenNhanVien.getText());
        NV.setTaiKhoan(txtTaiKhoan.getText());
        NV.setNgayVaoLam(txtNgayVaoLam.getText());
        NV.setSoNgayNghi(Integer.parseInt(txtSoNgayNghi.getText()));

        DataIO.dsNhanVien.add(NV);

        NhanVien nv1 = DataIO.dsNhanVien.get(DataIO.dsNhanVien.size() - 1);
        model.addRow(new Object[]{
            nv1.getMaNhanVien(),nv1.getTenNhanVien(),nv1.getTenNhanVien(),
            nv1.getTaiKhoan(),nv1.getNgayVaoLam(),nv1.getSoNgayNghi()
        });

        DataIO.writeNNhanVien();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        int i = tableNhanVien.getSelectedRow();

        NhanVien nv = new NhanVien();
        nv.setMaNhanVien(txtMaNhanVien.getText());
        nv.setTenNhanVien(txtTenNhanVien.getText());
        nv.setTaiKhoan(txtTaiKhoan.getText());
        nv.setNgayVaoLam(txtNgayVaoLam.getText());
        nv.setSoNgayNghi(Integer.parseInt(txtSoNgayNghi.getText()));

        DataIO.dsNhanVien.set(i, nv);

        model.setValueAt(nv.getMaNhanVien(), i, 0);
        model.setValueAt(nv.getTenNhanVien(), i, 1);
        model.setValueAt(nv.getTaiKhoan(), i, 2);
        model.setValueAt(nv.getNgayVaoLam(), i, 3);
        model.setValueAt(nv.getSoNgayNghi(), i, 4);

        DataIO.writeNNhanVien();
    }//GEN-LAST:event_btnCapNhatActionPerformed

